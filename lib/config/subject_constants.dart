  // Helper to find the sub-subject (book) for a given chapter
  static String? getSubSubjectForChapter(String chapter) {
    for (final entry in chapters.entries) {
      if (entry.value.contains(chapter)) {
        return entry.key;
      }
    }
    return null;
  }
